-- ---------- TABELAS ----------

create table if not exists plans (
  id text primary key,
  name text not null,
  price numeric(10,2) not null default 0,
  classes_per_month integer not null default 0,
  highlight boolean not null default false
);

create table if not exists instructors (
  id text primary key,
  name text not null,
  modalities text[] not null default '{}'
);

create table if not exists students (
  id text primary key,
  name text not null,
  phone text default '',
  email text default '',
  birth_date date,
  gender text not null default 'masculino',
  plan_id text,
  join_date date,
  last_activity_date date,
  status text not null default 'ativo'
);

create table if not exists classes (
  id text primary key,
  name text not null,
  type text not null,
  modality text not null,
  instructor_id text,
  schedule text,
  capacity integer not null default 0
);

create table if not exists class_students (
  class_id text not null references classes(id) on delete cascade,
  student_id text not null references students(id) on delete cascade,
  primary key (class_id, student_id)
);

create table if not exists financial_entries (
  id text primary key,
  type text not null,
  category text not null,
  payment_method text not null,
  amount numeric(10,2) not null,
  date date not null,
  description text default ''
);

create table if not exists graduations (
  id text primary key,
  student_id text not null references students(id) on delete cascade,
  modality text not null,
  belt text not null,
  date date not null,
  instructor_id text,
  notes text
);

create table if not exists payment_configs (
  student_id text primary key references students(id) on delete cascade,
  monthly_fee numeric(10,2) not null default 100,
  payment_method text not null default 'Pix',
  notes text default ''
);

create table if not exists monthly_payments (
  id text primary key,
  student_id text not null references students(id) on delete cascade,
  month integer not null,
  year integer not null,
  paid boolean not null default false,
  paid_date date,
  amount_paid numeric(10,2),
  payment_method text default 'Pix',
  notes text default '',
  unique (student_id, month, year)
);

-- ---------- ROW LEVEL SECURITY ----------
-- Somente usuários autenticados (equipe da academia) têm acesso.

alter table plans enable row level security;
alter table instructors enable row level security;
alter table students enable row level security;
alter table classes enable row level security;
alter table class_students enable row level security;
alter table financial_entries enable row level security;
alter table graduations enable row level security;
alter table payment_configs enable row level security;
alter table monthly_payments enable row level security;

do $$
declare t text;
begin
  foreach t in array array[
    'plans','instructors','students','classes','class_students',
    'financial_entries','graduations','payment_configs','monthly_payments'
  ]
  loop
    execute format('drop policy if exists "equipe_full_access" on %I;', t);
    execute format(
      'create policy "equipe_full_access" on %I for all to authenticated using (true) with check (true);', t
    );
  end loop;
end $$;

